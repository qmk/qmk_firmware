	static void print_logo_narrow(void) {
		oled_set_cursor(0, 2);
		switch (get_highest_layer(default_layer_state)) {
			case _QWERTY:
				oled_write_P(PSTR("Qwrty"), false);
				break;
			case _COLEMAK:
				oled_write_P(PSTR("Colmk"), false);
				break;
			case _PROGRAMMER_DVORAK:
				oled_write_P(PSTR("P Dvk"), false);
				break;
			case _GAMING:
				oled_write_P(PSTR("Game "), false);
				break;
			default:
				oled_write_P(PSTR("Undef"), false);
		}

		/* wpm counter */
		oled_set_cursor(0,9);
		oled_write_P(PSTR("WPM:\n"), false);
		oled_write(get_u8_str(get_current_wpm(), ' '), false);
	}

bool shift_held = false;
bool ctrl_held = false;

static void print_status_narrow(void) {
	if (keymap_config.swap_lctl_lgui) {
		oled_write_ln_P(PSTR("MAC"), false);
	}
	else {
		oled_write_ln_P(PSTR(""), false);
		//oled_write_ln_P(PSTR("WIN"), false);
	}

	/* Print current layer */
	oled_set_cursor(0,2);
	oled_write("LAYER", false);

	oled_set_cursor(0,3);
    switch (get_highest_layer(layer_state)) {
        case _COLEMAK:
        case _QWERTY:
		case _PROGRAMMER_DVORAK:
		case _GAMING:
            oled_write_P(PSTR("Base "), false);
            break;
        case _RAISE:
            oled_write_P(PSTR("Raise"), false); 
            break;
        case _LOWER:
            oled_write_P(PSTR("Lower"), false);
            break;
        case _ADJUST:
            oled_write_P(PSTR("Adj  "), false);
            break;
        default:
            oled_write_P(PSTR("Undef"), false);
    }

	/* lock status */
	oled_set_cursor(0,5);
	if (led_usb_state.caps_lock) {
		oled_write("CAPS", true);
	} else if (is_caps_word_on()) {
		oled_write("WORD", true);
	} else {
		oled_write("\n", true);
	}
	oled_set_cursor(0,6);
	oled_write(shift_held? "SHIFT" : "\n", true);
	oled_set_cursor(0,7);
	oled_write(ctrl_held? "CTRL" : "\n", true);
	//oled_write_ln("Num", !led_usb_state.num_lock);
	//oled_write_ln("Scrl", led_usb_state.scroll_lock);

	/* KEYBOARD PET RENDER START */
	render_luna(0,11);
	/* KEYBOARD PET RENDER END */
}

oled_rotation_t oled_init_user(oled_rotation_t rotation) {
	return OLED_ROTATION_270;
}

bool oled_task_user(void) {
	/* KEYBOARD PET VARIABLES START */
	current_wpm_read = get_current_wpm();
	led_usb_state = host_keyboard_led_state();
	/* KEYBOARD PET VARIABLES END */

	if (is_keyboard_master()) { //Drashna's OLED timeout off code for animations
		if (timer_elapsed32(oled_timer) > 30000) {
			oled_off();
			return false;
		} else {
			oled_on();
		}
	} else {
		if (is_oled_on()) {
			oled_on();
		} else {
			oled_off();
		}
	}

	if (is_keyboard_master()) {
		print_status_narrow();
	} else {
		print_logo_narrow();
	}
	return false;
}
