/* Shows the name of the current layer and locks for the host (Cap etc.) */
static void render_info(void) {
    oled_write_P(PSTR("       GreatPad\n"), false);
    oled_write_P(PSTR("\n"), false);
    led_t led_state = host_keyboard_led_state();
    oled_write_P(led_state.caps_lock ? PSTR("Cap(x) ") : PSTR("Cap( ) "), false);
    oled_write_P(led_state.num_lock ? PSTR("Num(x) ") : PSTR("Num( ) "), false);
    oled_write_P(led_state.scroll_lock ? PSTR("Scrl(x)") : PSTR("Scrl( )"), false);
    oled_write_P(PSTR("\n"), false);

    switch (get_highest_layer(layer_state)) {
        case 0:
            render_LAYER_0();
            break;
        case 1:
            render_LAYER_1();
            break;
        case 2:
            render_LAYER_2();
            break;
        case 3:
            render_LAYER_3();
            break;
        case 4:
            render_LAYER_4();
            break;
        case 5:
            render_LAYER_5();
            break;
        case 6:
            render_LAYER_6();
            break;
        case 7:
            render_LAYER_7();
            break;
        case 8:
            render_LAYER_8();
            break;
        case 9:
            render_LAYER_9();
            break;
        default:
            render_LAYER_UNDEF();
    }
}

#define SHOW_LOGO 5000
bool oled_task_kb(void) {
    if (!oled_task_user()) {
        return false;
    }
    static bool finished_timer = false;
    if (!finished_timer && (timer_elapsed(startup_timer) < SHOW_LOGO)) {
        render_logo();
    } else {
        if (!finished_timer) {
            oled_clear();
            finished_timer = true;
        }
        render_info();
    }
    return false;
}
