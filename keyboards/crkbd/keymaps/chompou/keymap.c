bool process_record_user(uint16_t keycode, keyrecord_t *record) {
    switch (keycode) {
        case KC_QWERTY:
            if (record->event.pressed) {
                set_single_persistent_default_layer(_QWERTY);
            }
            return false;
        case KC_COLEMAK:
            if (record->event.pressed) {
            set_single_persistent_default_layer(_COLEMAK);
            }
            return false;
        case KC_NUMS:
            if (record->event.pressed) {
                layer_on(_NUMS);
            } else {
                layer_off(_NUMS);
            }
            return false;
        case KC_SYMBOLS:
            if (record->event.pressed) {
                layer_on(_SYMBOLS);
            } else {
                layer_off(_SYMBOLS);
            }
            return false;
        case KC_FUNCS:
            if (record->event.pressed) {
                layer_on(_FUNCS);
            } else {
                layer_off(_FUNCS);
            }
            return false;

            /* KEYBOARD PET STATUS START */

        case KC_LCTL:
        case KC_RCTL:
            if (record->event.pressed) {
                isSneaking = true;
            } else {
                isSneaking = false;
            }
            break;
        case KC_SPC:
            if (record->event.pressed) {
                isJumping  = true;
                showedJump = false;
            } else {
                isJumping = false;
            }
            break;

            /* KEYBOARD PET STATUS END */
    }
    return true;
}
