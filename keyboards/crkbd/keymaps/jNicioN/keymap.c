static void print_logo_narrow(void) {
    oled_set_cursor(0,0);
    oled_write("WPM:\n", false);

    /* wpm counter */
    uint8_t n = get_current_wpm();  
    char    wpm_str[4];
    oled_set_cursor(0, 1);
    wpm_str[3] = '\0';
    wpm_str[2] = '0' + n % 10;
    wpm_str[1] = '0' + (n /= 10) % 10;
    wpm_str[0] = '0' + n / 10;
    oled_write(wpm_str, false);    
}

