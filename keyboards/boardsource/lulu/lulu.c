void process_layer_state(void) {
    switch (get_highest_layer(layer_state)) {
        case _QWERTY:
            render_layer1_logo();
            break;
        case _LOWER:
            render_layer2_logo();
            break;
        case _RAISE:
            render_layer3_logo();
            break;
        case _ADJUST:
            render_layer4_logo();
            break;
    }
}

bool oled_task_kb(void) {
    if (!oled_task_user()) { return false; }
    if (is_keyboard_master()) {
        process_layer_state();
    } else {
        render_logo();
    }
    return false;
}
