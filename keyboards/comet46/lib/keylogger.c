void set_keylog(uint16_t keycode)
{
  char name[5] = "?";
  uint16_t len_code_to_name = sizeof(code_to_name)/sizeof(*code_to_name);

  if (keycode < len_code_to_name) {
    snprintf(name, sizeof(name), code_to_name[keycode]);
  } else if (keycode >= len_code_to_name) {
    snprintf(name, sizeof(name), "QMK");
  }

  // update keylog
  snprintf(keylog, sizeof(keylog), "KC: %-4s  ID: %d", name, keycode);
}

const char *read_keylog(void) {
  return keylog;
}
