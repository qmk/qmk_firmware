bool process_record_user(uint16_t keycode, keyrecord_t *record) {
  switch (keycode) {
    case LOGO_LED_ON:
        if (record->event.pressed) {
             writePinHigh(B7);	
        } 
        break;
    case LOGO_LED_OFF:
        if (record->event.pressed) {
             writePinLow(B7);	
        } 
        break;
  }
  return true;
};
