uint8_t selected_option = 0;
uint8_t menu = 0;

bool process_record_kb(uint16_t keycode, keyrecord_t *record) {
    switch (keycode) {
        case PB_2:
            if (record->event.pressed) {
				if(menu == 0){
					selected_option++;
        			if (selected_option >= 3) selected_option = 0;
				}
                else if (menu == 1){
					rgb_matrix_toggle();
				}
				else if (menu == 2){
					rgb_matrix_increase_val();
				}
				else if (menu == 3){
					rgb_matrix_step();
				}
            }
            break;
		case PB_1:
            if (record->event.pressed) {
				if(menu == 0){
					if (selected_option == 0) selected_option = 2;
					else selected_option--;
				}
				else if (menu == 1){
					rgb_matrix_toggle();
				}
				else if (menu == 2){
					rgb_matrix_decrease_val();
				}
				else if (menu == 3){
					rgb_matrix_step_reverse();
				}
            }
            break;
		case PB_3:
            if (record->event.pressed) {
				if(menu == 0) menu = selected_option+1;
				else menu = 0;
		}
    }
    return true;
}

static void render_menu(void) {
	char buffer[32];
	switch (menu) {
        case 0: // Main
			const char* options[3] = {"RGB", "Brightness", "Animation"};
            for (uint8_t i = 0; i < 3; i++) {
				if (i == selected_option) {
					oled_write_P(PSTR("> "), false);
				} else {
					oled_write_P(PSTR("  "), false);
				}
				oled_write_P(options[i], false);
				oled_write_P(PSTR("\n"), false);
			}
            break;
        case 1: // RGB
			oled_write_P(PSTR("                    "), false);
			oled_write_P(PSTR("                    "), false);
			oled_write_P(PSTR("                    "), false);
			oled_set_cursor(0, 0);
            snprintf(buffer, sizeof(buffer), "RGB: %s", rgb_matrix_is_enabled() ? "On" : "Off");
			oled_write(buffer, false);
            break;
        case 2: // Brightness
            uint8_t brightness = rgb_matrix_get_val();
            uint8_t brightness_percent = (brightness * 100) / 255;
			oled_write_P(PSTR("                    "), false);
			oled_write_P(PSTR("                    "), false);
			oled_write_P(PSTR("                    "), false);
			oled_set_cursor(0, 0);
            snprintf(buffer, sizeof(buffer), "Brightness: %d%%", brightness_percent);
			oled_write(buffer, false);
            break;
        case 3: // Animation
			oled_write_P(PSTR("                    "), false);
			oled_write_P(PSTR("                    "), false);
			oled_write_P(PSTR("                    "), false);
			oled_set_cursor(0, 0);
            snprintf(buffer, sizeof(buffer), "Animation: %d", (uint8_t)rgb_matrix_get_mode());
			oled_write(buffer, false);
            break;
    }

}

