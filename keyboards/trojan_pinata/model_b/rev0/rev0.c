// run this whenever the oled needs to update
bool oled_task_kb(void) {
    if (!oled_task_user()) {
        return false;
    }
    keyboard_config.raw = eeconfig_read_kb(); // read saved config
    display_mode = keyboard_config.display_mode_saved;
    switch (display_mode) {
        case MTNS:
            mountains();
            break;
        case INFO:
            info();
            break;
        case LOGO:
            logo_with_layer();
            break;
        case MEW:
            mew();
            break;
        case CEL:
            celebi();
            break;
        case RAY:
            rayquaza();
            break;
        default:
            logo();
    }
    return false;
}


// render this on boot/reset
void oled_render_boot(bool bootloader) {
    oled_clear();
    logo();
    oled_render_dirty(true);
}

// run this on boot/reset
bool shutdown_kb(bool jump_to_bootloader) {
    if (!shutdown_user(jump_to_bootloader)) {
        return false;
    }
    return false;
}

// change depending on if OLED_MOD is pressed
bool process_record_kb(uint16_t keycode, keyrecord_t *record) {
    if (!process_record_user(keycode, record)) {
        return false;
    }
    if (keycode == OLED_MOD) {
        if (record->event.pressed) {
            oled_clear();
            if (display_mode == NUM_MODES - 1) {
                display_mode = 0;
            }
            else {
                display_mode++;
            }
            keyboard_config.display_mode_saved = display_mode;
            eeconfig_update_kb(keyboard_config.raw);  // update config when screen changes
        }
    }
    return true;
}

