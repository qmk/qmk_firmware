bool alt_tabbing = false;
bool woke_mode = false;
uint16_t rgb_edit_timer = 0;
uint16_t last_rgb_char = 0;

bool process_record_user(uint16_t keycode, keyrecord_t *record) {
    switch (keycode) {
        case QWERTY:
            if (record->event.pressed) {
                set_single_persistent_default_layer(_QWERTY);
            }
            return false;
        case COLEMAK:
            if (record->event.pressed) {
                set_single_persistent_default_layer(_COLMAK);
            }
            return false;
        case SYM:
            if (record->event.pressed) {
                layer_on(_SYM);
            } else {
                layer_off(_SYM);
            }
            return false;
        case MY_SPC:
            if (!record->event.pressed) {
                if(alt_tabbing) {
                    unregister_code(KC_LALT);
                    alt_tabbing = false;
                }
            }
            return true;
        case MISC:
            if (record->event.pressed) {
                layer_on(_MISC);
            } else {
                layer_off(_MISC);
            }
            return false;
        case DOTSPC:
            if (record->event.pressed) {
                tap_code(KC_DOT);
                tap_code(KC_SPC);
                set_oneshot_mods(MOD_LSFT);
            }
            return false;
        case COMMSPC:
            if (record->event.pressed) {
                tap_code(KC_COMM);
                tap_code(KC_SPC);
            }
            return false;
        case EXLMSPC:
            if (record->event.pressed) {
                tap_code16(KC_EXLM);
                tap_code(KC_SPC);
                set_oneshot_mods(MOD_LSFT);
            }
            return false;
        case QUESSPC:
            if (record->event.pressed) {
                tap_code16(KC_QUES);
                tap_code(KC_SPC);
                set_oneshot_mods(MOD_LSFT);
            }
            return false;
        case QUOT:
            if (record->event.pressed) {
                tap_code(KC_QUOT);
                tap_code(KC_QUOT);
                tap_code(KC_LEFT);
            }
            return false;
        case DQUOT:
            if (record->event.pressed) {
                tap_code16(KC_DQUO);
                tap_code16(KC_DQUO);
                tap_code(KC_LEFT);
            }
            return false;
        case PRN:
            if (record->event.pressed) {
                tap_code16(KC_LPRN);
                tap_code16(KC_RPRN);
                tap_code(KC_LEFT);
            }
            return false;
        case BRC:
            if (record->event.pressed) {
                tap_code(KC_LBRC);
                tap_code(KC_RBRC);
                tap_code(KC_LEFT);
            }
            return false;
        case CBR:
            if (record->event.pressed) {
                tap_code16(KC_LCBR);
                tap_code16(KC_RCBR);
                tap_code(KC_LEFT);
            }
            return false;
        case LTGT:
            if (record->event.pressed) {
                tap_code16(KC_LT);
                tap_code16(KC_GT);
                tap_code(KC_LEFT);
            }
            return false;
        case LTGTC:
            if (record->event.pressed) {
                tap_code16(KC_LT);
                tap_code(KC_SLSH);
                tap_code16(KC_GT);
                tap_code(KC_LEFT);
            }
            return false;
        case GRV:
            if (record->event.pressed) {
                tap_code(KC_GRV);
                tap_code(KC_GRV);
                tap_code(KC_LEFT);
            }
            return false;
        case AS_TAB:
            if (record->event.pressed) {
                register_code(KC_LSFT);
            } else {
                unregister_code(KC_LSFT);
            }
        case A_TAB:
            if (record->event.pressed) {
                register_code(KC_LALT);
                register_code(KC_TAB);
                alt_tabbing = true;
            } else {
                unregister_code(KC_TAB);
            }
            return false;
        case WOKE:
            if (!record->event.pressed) {
                woke_mode = !woke_mode;
            }
            return false;
        case RGB_TOG:
        case RGB_MOD:
        case RGB_HUD:
        case RGB_HUI:
        case RGB_SAD:
        case RGB_SAI:
        case RGB_VAI:
        case RGB_VAD:
            rgb_edit_timer = timer_read();
            last_rgb_char = keycode;
            return true;
    }

    if (woke_mode && ((keycode >= KC_A && keycode <= KC_Z) || keycode == MY_BSPC)) {
        if (record->event.pressed) {
            tap_code(KC_CAPS);
        }
    }

    return true;
}

layer_state_t layer_state_set_user(layer_state_t state) {
    state = update_tri_layer_state(state, _NAV, _NUM, _ADJUST);
    return state;
}

