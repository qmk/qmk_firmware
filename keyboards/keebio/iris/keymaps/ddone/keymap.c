bool process_record_user(uint16_t keycode, keyrecord_t *record) {
  switch (keycode) {
    case LOWER:
      if (record->event.pressed) {
        layer_on(_LOWER);
        update_tri_layer(_LOWER, _RAISE, _ADJUST);
      } else {
        layer_off(_LOWER);
        update_tri_layer(_LOWER, _RAISE, _ADJUST);
      }
      return false;
      break;
    case RAISE:
      if (record->event.pressed) {
        layer_on(_RAISE);
        update_tri_layer(_LOWER, _RAISE, _ADJUST);
      } else {
        layer_off(_RAISE);
        update_tri_layer(_LOWER, _RAISE, _ADJUST);
      }
      return false;
      break;
    case ADJUST:
      if (record->event.pressed) {
        layer_on(_ADJUST);
      } else {
        layer_off(_ADJUST);
      }
      return false;
      break;
  }
  return true;
}

bool get_permissive_hold(uint16_t keycode, keyrecord_t *record) {
    switch (keycode) {
	case SFTESC:
	    return true;
	default:
	    return false;
    }
}



bool encoder_update_user(uint8_t index, bool clockwise) {

	switch (get_highest_layer(layer_state)) {
	    case _LOWER:
		if (clockwise) {
		    tap_code(KC_WH_D);
		} else {
		    tap_code(KC_WH_U);
		}
		break;
	    default:
		if (clockwise) {
		    tap_code(KC_PGDN);
		} else {
		    tap_code(KC_PGUP);
		}
	}
    return true;
}
