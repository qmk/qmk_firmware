#define IS_LAYER_ON(layer)  ((layer_state) & (1<<(layer)))
#define IS_LAYER_OFF(layer) ((!layer_state) & (1<<(layer)))

void update_tri_layer(uint8_t layer1, uint8_t layer2, uint8_t layer3) {
  if (IS_LAYER_ON(layer1) && IS_LAYER_ON(layer2)) {
    layer_on(layer3);
  } else {
    layer_off(layer3);
  }
}
const macro_t *action_get_macro(keyrecord_t *record, uint8_t id, uint8_t opt)
{
  // MACRODOWN only works in this function
      switch(id) {
        case M_QW:
          if (record->event.pressed) {
            #ifdef AUDIO_ENABLE
              play_notes(&tone_qw, 4, false);
            #endif
            default_layer_set(1UL<<_QW);
          }
          break;
        case M_CM:
          if (record->event.pressed) {
            #ifdef AUDIO_ENABLE
              play_notes(&tone_cm, 6, false);
            #endif
            default_layer_set(1UL<<_CM);
          }
          break;
        case M_DV:
          if (record->event.pressed) {
            #ifdef AUDIO_ENABLE
              play_notes(&tone_dv, 8, false);
            #endif
            default_layer_set(1UL<<_DV);
          }
          break;
        case M_LW:
          if (record->event.pressed) {
            layer_on(_LW);
            update_tri_layer(_LW, _RS, _AD);
          } else {
            layer_off(_LW);
            update_tri_layer(_LW, _RS, _AD);
          }
          break;
        case M_RS:
          if (record->event.pressed) {
            layer_on(_RS);
            update_tri_layer(_LW, _RS, _AD);
          } else {
            layer_off(_RS);
            update_tri_layer(_LW, _RS, _AD);
          }
          break;
        case M_BL:
          if (record->event.pressed) {
            register_code(KC_RSFT);
            #ifdef BACKLIGHT_ENABLE
              backlight_step();
            #endif
          } else {
            unregister_code(KC_RSFT);
          }
        break;
      }
    return MACRO_NONE;
};

